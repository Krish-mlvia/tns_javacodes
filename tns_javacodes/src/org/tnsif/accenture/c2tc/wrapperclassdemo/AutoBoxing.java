package org.tnsif.accenture.c2tc.wrapperclassdemo;

public class AutoBoxing {
	public static void main(String[] args) {

        int a = 10;          // primitive
        Integer obj = a;     // autoboxing

        System.out.println(obj);
    }

}

//wrapper class in java is a mechanism which converts primitive to object (autoboxing)  and object to primitive (auto unboxing)
